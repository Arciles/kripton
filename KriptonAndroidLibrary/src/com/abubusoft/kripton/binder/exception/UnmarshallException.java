package com.abubusoft.kripton.binder.exception;

public class UnmarshallException extends Exception {
	
	private static final long serialVersionUID = -2996570457431724776L;

	public UnmarshallException() {
	}

	public UnmarshallException(String arg0) {
		super(arg0);
	}

	public UnmarshallException(Throwable arg0) {
		super(arg0);
	}

	public UnmarshallException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
